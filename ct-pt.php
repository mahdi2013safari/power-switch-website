                    <h2 class="title">Current Transformer ( C.T. )</h2>
                    <h5 class="description">
                    The Current Transformer ( C.T. ), is a type of “instrument transformer” that is designed to produce an alternating current in its secondary winding which is proportional to the current being measured in its primary. Current transformers reduce high voltage currents to a much lower value and provide a convenient way of safely monitoring the actual electrical current flowing in an AC transmission line using a standard ammeter. The principal of operation of a basic current transformer is slightly different from that of an ordinary voltage transformer.<br/>

                    typical current transformer

                    Unlike the voltage or power transformer looked at previously, the current transformer consists of only one or very few turns as its primary winding. This primary winding can be of either a single flat turn, a coil of heavy duty wire wrapped around the core or just a conductor or bus bar placed through a central hole as shown.<br/>

                    Due to this type of arrangement, the current transformer is often referred too as a “series transformer” as the primary winding, which never has more than a very few turns, is in series with the current carrying conductor supplying a load.

                    The secondary winding however, may have a large number of coil turns wound on a laminated core of low-loss magnetic material. This core has a large cross-sectional area so that the magnetic flux density created is low using much smaller cross-sectional area wire, depending upon how much the current must be stepped down as it tries to output a constant current, independent of the connected load.<br/>

                    The secondary winding will supply a current into either a short circuit, in the form of an ammeter, or into a resistive load until the voltage induced in the secondary is big enough to saturate the core or cause failure from excessive voltage breakdown.<br/>

                    Unlike a voltage transformer, the primary current of a current transformer is not dependent of the secondary load current but instead is controlled by an external load. The secondary current is usually rated at a standard 1 Ampere or 5 Amperes for larger primary current ratings.<br/><br/>

                    <b>There are three basic types of current transformers: wound, toroidal and bar.</b><br/><br/>

                    <b>Wound Current Transformer</b> – The transformers primary winding is physically connected in series with the conductor that carries the measured current flowing in the circuit. The magnitude of the secondary current is dependent on the turns ratio of the transformer.<br/><br/>
                    <b>Toroidal Current Transformer</b> – These do not contain a primary winding. Instead, the line that carries the current flowing in the network is threaded through a window or hole in the toroidal transformer. Some current transformers have a “split core” which allows it to be opened, installed, and closed, without disconnecting the circuit to which they are attached.<br/><br/>
                    <b>Bar-type Current Transformer</b> – This type of current transformer uses the actual cable or bus-bar of the main circuit as the primary winding, which is equivalent to a single turn. They are fully insulated from the high operating voltage of the system and are usually bolted to the current carrying device.<br/><br/>
