                        فیوز یک وسیله حفاظتی است که در تاسیات برقی جهت جلوگیری از صدمه دیدن و معیوب شدن
                        وسایل و نیز برای قطع کردن دستگاه ها و قسمت های معیوب به کار میروند.این وسیله باید طوری
                        انتخاب شود که در صورت عارضه در کوچکترین زمان ممکن آن بخش عارضه دیده را قطع کند. فیوز
                        ها نظر به زمان قطع آن به دو بخش تقسیم میگردد.
                        الف_ فیوز تند کار: این فیوز در کوتاه ترین زمان ممکن مصرف کننده از شبکه برقی قطع
                        میکند و معمولا در شبکه های روشنائی استفاده میشود.
                        ب_ فیوز کند کار: این فیوز زمان قطع بیشتری رانسبت به فیوز تند کار نیاز دارد و معمولا برای
                        قطع دوره های موتوری مورد استعمال قرار میگیرد. (موتور ها جریان چالانی بلند تری نسبت به جریان
                        نورمال دارد ).
                        اما فیوز از نظر ساختمان و شکل ظاهری و نوعیت کار کرد خویش به انواع ذیل تقسیم میشوند.
                        الف_ فیوز ذوب شووونده )فشنگی (:این فیوز از یک سیم حراتی ساخته شده که با مقیاس معین
                        جریان این سیم ذوب شده و دوره قطع میکند. فیوزهای قطع سریع باعلامت F و قطع تاخیری آن با
                        علامت  نشان داده میشود. و شکل آن قرار ذیل است.
                    </h5>
                    <img src="img/fashandfuse.jpg" class="img img-raised img-fluid">
                    <h5 class="description" dir="rtl">
                        <b class="info-title">ب_ فیوز اتومات:</b><br/>
                        فیوز اتوماتیک یا الفا نوعی فیوز خودکار میباشد که باعبور جریان بیش از
                        حد مجاز آن دوره را قطع نموده. اما دوباره به شکل شصتی انرا به داخل فشار داد و ارتباط را برقرار
                        نمود. و ساختمان آن طوری است که در داخل آن دو قسمت وجود دارد, قسمت مقناطیسی و قسمت
                        حرارتی(بیمیتای) که قسمت مقناطیسی آن شارتی و قسمت بیمیتال آن اضافه باری را قطع میکند که نمای
                        ظاهری آن در ذیل آورده شده است.
                    </h5>
                    <img src="img/fuse.jpg" class="img img-raised img-fluid">
                    <h5 class="description" dir="rtl">
                        <b class="info-title">ج_ فیوز میناتوری:</b><br/>
                        فیوز میناتوری نوع فیوز اتوماتیک است که از نظر ساختمان داخلی با آن
                        شباهت دارد و از سه قسمت رله مقناطیسی(قطع کننده جریان شارتی)، بیمیتال (قطع کننده اضافه باری)
                        و کلید تشکیل شده است. که کلید های آن در دو نوع L و G سااخته شاده است. که سویچ L مانند آن در
                        دوره های که نیاز به قطع سریع باشد بکار میرورد و تند کار است. اما نوع با سویچ G مانند آن در دوره های موتوری به دلیل تند کار بودن آن انتخاب میگردد.
                        که این فیوزها از نظر زمان قطع خود به سه کلاسB, A وCتقسیم می شود،که کلاس Aآن دقت بالا دارد و زمان قطع  آن سریع می باشد.وکلاس Cآن دقت پاین داشته و زمان قطع با تآخیر می باشد یعنی بعد از اضافه باری سریع قطع نشده اندکی با تاخیری عمل می کند اما در جریان های شارتی سریع عمل نموده دوره را قطع میکند.
